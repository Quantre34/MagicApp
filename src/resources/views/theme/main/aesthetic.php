                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;Regular check-ups with the doctor are conducted to monitor weight loss and the patient's health condition.</font>
                                </font>
                            </li>
                        </ul>
