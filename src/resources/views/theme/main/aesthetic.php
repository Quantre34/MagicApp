                        <h1 style="text-align: justify;">Gastric Balloon</h1>
                        <p style="text-align: justify;">It is a minimally invasive, non-surgical treatment that involves inserting an FDA-approved device into the stomach using endoscopic methods.</p>
                        <p style="text-align: justify;">When the candidate is obese and tries various weight loss methods but fails, the gastric balloon option is applied as the first solution. This option helps patients lose 30 percent of the desired weight in 6 months to 1 year. After 6 months to a year, the surgeon removes the inserted balloon using endoscopy.</p>
                        <p style="text-align: justify;">This method also reduces the risk of other health problems such as heart disease, stroke, sleep apnea, type 2 diabetes, and high blood pressure. The person can return to normal activities just a few days after the balloon insertion.</p>
                        <h3><br />
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">What is a Gastric Balloon?</font>
                            </font>
                        </h3>
                        <p><strong style="box-sizing:border-box; font-weight:700">
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">Gastric Balloon</font>
                                </font>
                            </strong>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;is a non-surgical method used in the treatment of obesity. This method allows the person to feel full with less food through a silicone balloon that is temporarily placed in the stomach and inflated. The gastric balloon is used to help with weight loss and to facilitate weight management.</font>
                            </font>
                        </p>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">How does the gastric balloon work?</font>
                            </font>
                        </h3>
                        <p>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">The gastric balloon takes up space in the stomach cavity and reduces the stomach's capacity. This makes the person feel full with a smaller amount of food, thereby reducing calorie intake. The gastric balloon is usually left in the stomach for 6 months to 1 year, and patients are encouraged to lose weight during this period.</font>
                            </font>
                        </p>
                        <p>&nbsp;</p>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Gastric Balloon Procedure</font>
                            </font>
                        </h3>
                        <p><strong style="box-sizing:border-box; font-weight:700">
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">1. Preparation:</font>
                                </font>
                            </strong></p>
                        <ul>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">Consultation:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;The first step is to consult with a gastroenterologist or bariatric surgeon. The patient's health condition is assessed to determine if they are suitable for a gastric balloon.</font>
                                </font>
                            </li>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">Tests:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;Endoscopy and other necessary tests are performed. These tests provide information about the stomach and overall health condition.</font>
                                </font>
                            </li>
                        </ul>
                        <p><br /><strong style="box-sizing:border-box; font-weight:700">
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">2. Insertion:</font>
                                </font>
                            </strong></p>
                        <ul>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">Endoscopy:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;The gastric balloon is inserted using an endoscope. Endoscopy is a thin tube with a camera at the end that passes into the stomach through the mouth.</font>
                                </font>
                            </li>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">Inflation:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;Once the balloon is placed in the stomach, it is inflated with sterile saline (saltwater). The balloon's size is adjusted according to the patient's stomach capacity and treatment plan.</font>
                                </font>
                            </li>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">Procedure:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;The procedure usually takes 20-30 minutes and is performed under sedation, so the patient does not feel any discomfort.</font>
                                </font>
                            </li>
                        </ul>
                        <p><br /><strong style="box-sizing:border-box; font-weight:700">
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">3. Recovery and Follow-up:</font>
                                </font>
                            </strong></p>
                        <ul>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
                                        <font style="box-sizing:border-box; vertical-align:inherit">First days:</font>
                                    </font>
                                </strong>
                                <font style="box-sizing:border-box; vertical-align:inherit">
                                    <font style="box-sizing:border-box; vertical-align:inherit">&nbsp;Nausea and mild discomfort may be felt in the first days after the procedure. This condition usually resolves within a few days.</font>
                                </font>
                            </li>
                            <li><strong style="box-sizing:border-box; font-weight:700">
                                    <font style="box-sizing:border-box; vertical-align:inherit">
