                        <h1 style="text-align: justify;">
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">What is Obesity?</font>
                            </font>
                        </h1>
                        <p><b>Obesity</b> is a serious medical condition characterized by excessive body fat
                            accumulation, which can negatively impact health. It is usually measured using the Body Mass
                            Index (BMI), and a BMI over 30 is defined as obesity. Obesity is a common health problem
                            worldwide and is associated with many serious health issues such as heart disease, diabetes,
                            hypertension, and certain types of cancer.</p>
                        <p>Surgery for <b>stomach reduction</b> is performed on patients with a body mass index over 30,
                            who, in addition to obesity, have health conditions such as diabetes, hypertension, asthma,
                            etc.</p>
                        <p>If we were to give an average percentage, approximately 80% of your stomach is removed in
                            this operation. Another name for this operation is stomach reduction.</p>
                        <p>With stomach reduction surgery, you achieve the feeling of fullness with a small amount of
                            food. Besides reducing the volume of the stomach, the removal of part of the stomach,
                            especially the part called the fundus, offers improvement and in many cases complete
                            recovery from diseases such as type 2 diabetes, hypertension, and high cholesterol levels,
                            which are particularly diseases associated with obesity.</p>
                        <p>To perform these operations in a short and safe time, the latest technology materials are
                            used. The most important is the instrument called Stapler, which ensures complete healing of
                            the stomach and prevents leaks after surgery.</p>
                        <p>Before undergoing stomach reduction surgery, all necessary tests and checks must be performed
                            by the doctor who will perform the surgery. The doctor assesses whether the patient is
                            physiologically and psychologically ready for stomach reduction surgery. After the surgery,
                            you will get rid of weight-related disorders.</p>
                        <h2>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">How does obesity occur?</font>
                            </font>
                        </h2>
                        <p>Obesity occurs as a result of an energy imbalance. When the body expends less energy than the
                            calories consumed, the excess energy is stored as fat, leading to weight gain and obesity in
                            the long term. The main causes of obesity include:</p>
                        <ul>
                            <li><b>Poor nutrition</b>: Excessive consumption of high-calorie, fatty, and sugary foods.
                            </li>
                            <li><b>Lack of physical activity</b>: Insufficient level of physical activity.</li>
                            <li><b>Genetic factors</b>: The risk of obesity is higher in people with a family history of
                                obesity.</li>
                            <li><b>Metabolic factors</b>: Metabolic diseases and hormonal imbalances.</li>
                            <li><b>Environmental factors</b>: Consumption of fast foods, large portions, and the
                                prevalence of processed foods.</li>
                            <li><b>Psychological factors</b>: Stress, depression, and emotional eating habits.</li>
                        </ul>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Effects of obesity on
                                    health</font>
                            </font>
                        </h3>
                        <p>Obesity can have many negative effects on the body. Among them are:</p>
                        <ul>
                            <li><b>Heart diseases</b>: Obesity increases the risk of coronary artery disease and heart
                                attack.</li>
                            <li><b>Type 2 diabetes</b>: Insulin resistance can develop in the body, making blood sugar
                                control difficult.</li>
                            <li><b>Hypertension</b>: High blood pressure is more common in obese individuals.</li>
                            <li><b>Cancer</b>: The risk of certain types of cancer (e.g., breast, colon, and endometrial
                                cancer) is increased.</li>
                            <li><b>Breathing problems</b>: Respiratory diseases such as sleep apnea and asthma.</li>
                            <li><b>Joint problems</b>: Arthritis can develop in joints such as knees and hips due to
                                overload.</li>
                            <li><b>Psychological effects</b>: Depression, low self-esteem, and social isolation are more
                                common in obese individuals.</li>
                        </ul>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Treatment and Management of
                                    Obesity</font>
                            </font>
                        </h3>
                        <p>Management of obesity usually involves lifestyle changes, diet, and exercise. Below are the
                            methods commonly used in the management of obesity:</p>
                        <ul>
                            <li><b>Diet and nutrition</b>: Following a healthy and balanced diet, avoiding high-calorie
                                and processed foods.</li>
                            <li><b>Physical activity</b>: Regular exercise and increasing daily activities.</li>
                            <li><b>Behavioral changes</b>: Managing unhealthy eating habits and emotional eating.</li>
                            <li><b>Medical treatment</b>: In some cases, doctors may recommend medications to support
                                weight loss.</li>
                            <li><b>Surgical intervention</b>: Bariatric surgery may be an option in severe cases of
                                obesity.</li>
                        </ul>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Recommendations for
                                    preventing obesity</font>
                            </font>
                        </h3>
                        <ul>
                            <li><b>Balanced diet</b>: Following a diet rich in fruits, vegetables, whole grains, and
                                lean proteins.</li>
                            <li><b>Regular exercise</b>: Engage in at least 150 minutes of moderate aerobic activity per
                                week.</li>
                            <li><b>Healthy habits</b>: Get regular sleep, manage stress, and drink enough water.</li>
                            <li><b>Education and awareness</b>: Be informed about obesity and healthy living.</li>
                        </ul>
                        <h3><br style="box-sizing:border-box" />
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Techniques used in the
                                    treatment of obesity</font>
                            </font>
                        </h3>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Stomach reduction
                                    (Gastrectomy)</font>
                            </font>
                        </h3>
                        <p><b>Stomach reduction</b>, commonly known as "gastric sleeve surgery," is a surgical
                            procedure where a large portion of the stomach is removed to create a tube-shaped stomach.
                            This reduces the capacity of the stomach, making the person feel full with less food.</p>
                        <p><b>Advantages</b></p>
                        <ul>
                            <li>Provides permanent weight loss,</li>
                            <li>Helps in the recovery of health problems related to obesity.</li>
                        </ul>
                        <p><b>Risks</b></p>
                        <ul>
                            <li>Surgical complications such as infection, bleeding, and leakage</li>
                            <li>Vitamin and mineral deficiencies.</li>
                        </ul>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Gastric bypass (Gastric
                                    bypass)</font>
                            </font>
                        </h3>
                        <p><b>Gastric bypass</b> is another surgical method used for treating obesity. In this
                            procedure, the stomach is turned into a small pouch, and the small intestine is connected
                            directly below this pouch. In this way, food bypasses most of the stomach and the initial
                            part of the small intestine.</p>
                        <p><b>Advantages</b></p>
                        <ul>
                            <li>Provides rapid and effective weight loss.</li>
                            <li>Helps in the recovery of metabolic diseases such as type 2 diabetes.</li>
                        </ul>
                        <p><b>Risks</b></p>
                        <ul>
                            <li>Infection</li>
                            <li>Intestinal obstruction</li>
                            <li>Dumping syndrome (rapid emptying of the stomach)</li>
                            <li>Vitamin and mineral deficiencies.</li>
                        </ul>
                        <h3><b>Gastric balloon</b></h3>
                        <p><b>Gastric balloon</b> is a non-surgical weight loss method. In this method, a silicone
                            balloon is placed in the stomach and filled with liquid or air. The balloon occupies space
                            in the stomach, allowing the person to feel full with less food.</p>
                        <p><b>Advantages</b></p>
                        <ul>
                            <li>Does not require surgery.</li>
                            <li>Has a short recovery time</li>
                            <li>Is reversible as it is a temporary solution.</li>
                        </ul>
                        <p><b>Risks</b></p>
                        <ul>
                            <li>Nausea and vomiting</li>
                            <li>Stomach ulcers.</li>
                            <li>Deflation or slipping of the balloon.</li>
                        </ul>
                        <h3>
                            <font style="box-sizing:border-box; vertical-align:inherit">
                                <font style="box-sizing:border-box; vertical-align:inherit">Stomach Botox (Gastric
                                    Botox)</font>
                            </font>
                        </h3>
                        <p><b>Stomach Botox</b> is a minimally invasive weight loss method. In this technique, botulinum
                            toxin is injected into specific areas of the stomach, slowing its emptying and reducing
                            muscle contractions, making the person feel full for longer.</p>
                        <p><b>Advantages</b></p>
                        <ul>
                            <li>Does not require surgery</li>
                            <li>Has a short recovery time</li>
                            <li>Low risk of complications.</li>
                        </ul>
                        <p><b>Risks</b></p>
                        <ul>
                            <li>Temporary effects</li>
                            <li>Pain or discomfort at the injection site</li>
                            <li>Rare allergic reactions.</li>
                        </ul>
                        <p>&nbsp;</p>
                        <p>Obesity is a condition that has serious effects on both individual and public health.
                            However, it is possible to cope with obesity with proper management and treatment. Adopting
                            a healthy lifestyle, exercising regularly, and eating a balanced diet are the most important
                            steps to prevent and manage obesity. You can consult a healthcare professional to learn more
                            about obesity and to evaluate your personal health condition.</p>
